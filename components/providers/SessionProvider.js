'use client';
import { createContext, useContext, useState, useEffect } from 'react';

const SessionContext = createContext();

export function SessionProvider({ children }) {
  const [isAuthenticated, setIsAuthenticated] = useState(false);
  const [currentUser, setCurrentUser] = useState(null);
  const [isLoading, setIsLoading] = useState(true);

  // Check for existing session on mount
  useEffect(() => {
    const checkSession = () => {
      try {
        const sessionData = localStorage.getItem('picfolio_session');
        if (sessionData) {
          const { user, timestamp } = JSON.parse(sessionData);
          const now = Date.now();
          const sessionAge = now - timestamp;
          
          // Session expires after 24 hours (optional)
          const maxAge = 24 * 60 * 60 * 1000; // 24 hours
          
          if (sessionAge < maxAge) {
            setIsAuthenticated(true);
            setCurrentUser(user);
          } else {
            // Session expired, clear it
            localStorage.removeItem('picfolio_session');
          }
        }
      } catch (error) {
        console.error('Error checking session:', error);
        localStorage.removeItem('picfolio_session');
      } finally {
        setIsLoading(false);
      }
    };

    checkSession();
  }, []);

  // Clean up session on tab close/window unload
  useEffect(() => {
    const handleBeforeUnload = () => {
      // Clear session when tab is closed
      localStorage.removeItem('picfolio_session');
    };

    const handleVisibilityChange = () => {
      // Clear session when tab becomes hidden (user switches tabs)
      if (document.hidden) {
        localStorage.removeItem('picfolio_session');
        setIsAuthenticated(false);
        setCurrentUser(null);
      }
    };

    window.addEventListener('beforeunload', handleBeforeUnload);
    document.addEventListener('visibilitychange', handleVisibilityChange);

    return () => {
      window.removeEventListener('beforeunload', handleBeforeUnload);
      document.removeEventListener('visibilitychange', handleVisibilityChange);
    };
  }, []);

  const login = (user, password) => {
    const sessionData = {
      user,
      password, // Store for API calls
      timestamp: Date.now()
    };
    
    localStorage.setItem('picfolio_session', JSON.stringify(sessionData));
    setIsAuthenticated(true);
    setCurrentUser(user);
  };

  const logout = () => {
    localStorage.removeItem('picfolio_session');
    setIsAuthenticated(false);
    setCurrentUser(null);
  };

  const value = {
    isAuthenticated,
    currentUser,
    isLoading,
    login,
    logout
  };

  return (
    <SessionContext.Provider value={value}>
      {children}
    </SessionContext.Provider>
  );
}

export function useSession() {
  const context = useContext(SessionContext);
  if (context === undefined) {
    throw new Error('useSession must be used within a SessionProvider');
  }
  return context;
}
